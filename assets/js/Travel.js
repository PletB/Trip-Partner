  function renderCards(districts) {
    container.innerHTML = '';
  
    const noResultMessage = document.getElementById('noResultMessage');
  
    if (districts.length === 0) {
      noResultMessage.style.display = 'block';
      return;
    } else {
      noResultMessage.style.display = 'none';
    }
  
    const sorted = [...districts].sort((a, b) => a.district.localeCompare(b.district, 'ko'));
    sorted.forEach(({ city, district }) => {
      const eng = regionEnglish[district] || "TRIP";
      const card = document.createElement('div');
      card.className = 'destination-card';
      card.innerHTML = `
        <img src="assets/images/Travel/${district}.jpg" alt="${district}">
        <h3>${eng}</h3>
        <p>${city} ${district}</p>
      `;
      container.appendChild(card);
    });
  }

