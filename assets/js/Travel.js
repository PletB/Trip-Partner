  // 필터 + 검색 통합 함수
  function filterAndRender() {
    const query = searchInput.value.trim();
    let filtered;

    if (selectedRegion === "전체") {
      filtered = allLocations;
    } else if (selectedRegion === "광역시") {
      filtered = allLocations.filter(loc => loc.city.includes("광역시") || loc.city.includes("세종"));
    } else if (selectedRegion === "충청도") {
      filtered = allLocations.filter(loc => loc.city.includes("충청"));
    } else if (selectedRegion === "경상도") {
      filtered = allLocations.filter(loc => loc.city.includes("경상"));
    } else if (selectedRegion === "전라도") {
      filtered = allLocations.filter(loc => loc.city.includes("전라"));
    } else if (selectedRegion === "제주도") {
      filtered = allLocations.filter(loc => loc.city.includes("제주"));
    } else {
      filtered = allLocations.filter(loc => loc.city === selectedRegion);
    }

    // 검색어가 있으면 추가 필터링
    if (query !== "") {
      filtered = filtered.filter(({ city, district }) => {
        const cityChosung = getChosung(city);
        const districtChosung = getChosung(district);
        return (
          city.includes(query) ||
          district.includes(query) ||
          cityChosung.includes(query) ||
          districtChosung.includes(query)
        );
      });
    }

    renderCards(filtered);
  }

